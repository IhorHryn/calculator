
const firstNumber = Number(prompt("Введите первое число:"));

const secondNumber = Number(prompt("Введите второе число:"));

const sum = firstNumber + secondNumber;
const diff = firstNumber - secondNumber;
const mult = firstNumber * secondNumber;
const div = firstNumber / secondNumber;

console.log(`Calculations are finished!\nSum:
${firstNumber} + ${secondNumber} = ${sum}\nDiff:
${firstNumber} - ${secondNumber} = ${diff}\nMult:
${firstNumber} * ${secondNumber} = ${mult}\nDiv:
${firstNumber} / ${secondNumber} = ${div}`);
